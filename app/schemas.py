"""
State-Memory Agent V1 — Data Schemas
All typed models used across the system.

Core principle: state variables are grounded in observable signals,
not self-reported by the model.
"""

from pydantic import BaseModel, Field
from datetime import datetime, timezone
from enum import Enum
from typing import Optional, Literal


# ──────────────────────────────────────────────
# Enums
# ──────────────────────────────────────────────

class ActiveMode(str, Enum):
    """V1 supports 3 modes only. V2 adds reflective, constrained, reactivated."""
    BASELINE = "baseline"
    CAUTIOUS = "cautious"
    RECOVERY = "recovery"


class Band(str, Enum):
    """Categorical band for projection from raw floats."""
    LOW = "low"
    MEDIUM = "medium"
    HIGH = "high"


# ──────────────────────────────────────────────
# Observable Signals (computed BEFORE LLM call)
# ──────────────────────────────────────────────

class ObservableSignals(BaseModel):
    """
    Pre-computed signals from the current turn.
    These are measured, not estimated by the model.
    Pass A receives these as anchors for state estimation.
    """
    turn_id: int
    user_message_length: int = 0
    tool_calls_this_turn: int = 0
    tool_errors_this_turn: int = 0
    tool_retries_this_turn: int = 0
    user_correction_detected: bool = False
    context_tokens_used: int = 0
    context_tokens_max: int = 128000
    session_turn_depth: int = 0
    prior_response_length: int = 0


# ──────────────────────────────────────────────
# State Register
# ──────────────────────────────────────────────

class StateRegister(BaseModel):
    """
    The agent's current operating condition.
    4 grounded variables + 1 optional derived metric.
    """
    session_id: str
    turn_id: int
    coherence: float = Field(ge=0.0, le=1.0, default=0.8)
    drift: float = Field(ge=0.0, le=1.0, default=0.0)
    tool_overload: float = Field(ge=0.0, le=1.0, default=0.0)
    context_fragmentation: float = Field(ge=0.0, le=1.0, default=0.0)
    active_mode: ActiveMode = ActiveMode.BASELINE
    # V1.1: computed AFTER Pass B by comparing draft vs final
    policy_pressure_proxy: Optional[float] = Field(
        default=None, ge=0.0, le=1.0
    )
    updated_at: datetime = Field(default_factory=lambda: datetime.now(timezone.utc))

    @classmethod
    def to_initial(cls, session_id: str) -> "StateRegister":
        """Create a fresh state for a new session."""
        return cls(
            session_id=session_id,
            turn_id=0,
            coherence=0.80,
            drift=0.00,
            tool_overload=0.00,
            context_fragmentation=0.00,
            active_mode=ActiveMode.BASELINE,
        )


# ──────────────────────────────────────────────
# State Delta
# ──────────────────────────────────────────────

class StateDelta(BaseModel):
    """
    Turn-to-turn motion. State alone = position. Delta = direction.
    """
    session_id: str
    turn_id: int
    delta_coherence: float
    delta_drift: float
    delta_tool_overload: float
    delta_context_fragmentation: float
    prior_mode: ActiveMode
    new_mode: ActiveMode
    cause_hint: str = ""
    timestamp: datetime = Field(default_factory=lambda: datetime.now(timezone.utc))
    @classmethod
    def from_states(
        cls,
        prior: StateRegister,
        new: StateRegister,
        cause_hint: str = "",
    ) -> "StateDelta":
        """Compute delta between two state registers."""
        return cls(
            session_id=new.session_id,
            turn_id=new.turn_id,
            delta_coherence=round(new.coherence - prior.coherence, 4),
            delta_drift=round(new.drift - prior.drift, 4),
            delta_tool_overload=round(new.tool_overload - prior.tool_overload, 4),
            delta_context_fragmentation=round(
                new.context_fragmentation - prior.context_fragmentation, 4
            ),
            prior_mode=prior.active_mode,
            new_mode=new.active_mode,
            cause_hint=cause_hint,
        )


