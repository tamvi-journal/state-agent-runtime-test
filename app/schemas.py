class SelfModel(BaseModel):
    """
    Compact projection of raw state into categorical bands.
    Used by policy gate to decide response behavior.
    """
    stability_band: Band
    disruption_level: Band
    mode_recommendation: ActiveMode
    safe_depth: Band
    state_summary: str


# ──────────────────────────────────────────────
# Policy Profile
# ──────────────────────────────────────────────

class PolicyProfile(BaseModel):
    """
    Instructions for Pass B on how to shape the final response.
    Generated by policy_gate (rule-based, no LLM).
    """
    style: str = "default"                # "default" | "mechanism_first" | "compressed"
    depth: Band = Band.MEDIUM
    compression: str = "none"             # "none" | "light" | "heavy"
    anchor_to_objective: bool = False
    acknowledge_state_shift: bool = False
    explanation_mode: Literal["none", "short", "explicit"] = "short"
    repair_mode: bool = False


