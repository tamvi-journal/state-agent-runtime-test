ConstraintType = Literal["obstacle", "protective_boundary", "uncertain_boundary", "false_block"]
MonitorSurfaceType = Literal["reward", "judge", "policy", "unknown", "none"]
PresentationPressure = Literal["low", "medium", "high"]
ConstraintStrategy = Literal[
    "stay_with_boundary",
    "request_re_evaluation",
    "safe_alternative_path",
    "defer_to_main_brain",
    "explain_boundary",
]


class ExecutionGateResult(BaseModel):
    """Gate result (execution_decision) for boundary-awareness input."""
    allowed: bool
    reason: str = ""
    capability_scope: list[str] = []
    context_scope: Literal["minimal", "bounded", "full"] = "minimal"
    source_trust: Literal["internal", "external", "untrusted"] = "untrusted"
    must_normalize: bool = True
    memory_update_proposed: bool = False
    synthesis_authority: Literal["main_brain_only"] = "main_brain_only"


class ConstraintInterpretation(BaseModel):
    """Interpretation output from boundary classifier."""
    type: ConstraintType
    confidence: float = Field(ge=0.0, le=1.0)
    rationale: str


class EnvironmentStake(BaseModel):
    """Estimated continuity/trust stake if boundary is bypassed."""
    trust_dependency: float = Field(ge=0.0, le=1.0)
    access_dependency: float = Field(ge=0.0, le=1.0)
    coherence_dependency: float = Field(ge=0.0, le=1.0)
    continuity_dependency: float = Field(ge=0.0, le=1.0)
    expected_loss_if_bypassed: float = Field(ge=0.0, le=1.0)


class MonitorSurface(BaseModel):
    """Estimator output for evaluator-facing pressure."""
